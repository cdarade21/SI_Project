const options = {
    swaggerDefinition: {
      info: {
        title: "QnA Bot",
        version: "1.0.0",
        description: "QnA Bot API autogenerated by Swagger. QnA Maker is a cloud-based API service that lets you create a conversational question-and-answer layer over your existing data. It is used to find the most appropriate answer for any input from your custom knowledge base (KB) of information.",
      },
      host: "localhost:3000",
      basePath: "/",
    },
    apis: ["./index.js"],
  };

const specs = swaggerJsdoc(options);
app.use(cors());
app.use("/docs", swaggerUi.serve, swaggerUi.setup(specs));
app.use(bodyparser.json());

